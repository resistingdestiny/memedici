                print(f"   ğŸ” vlayer proof created: {proof.session_id}")
                
                # Step 4: Verify the proof
                print("\nğŸ“‹ STEP 4: Verify Content Proof")
                print("-" * 40)
                
                verification_result = verify_agent_content(proof.session_id)
                
                print(f"   ğŸ” Verification result: {verification_result['verified']}")
                
                if verification_result["verified"]:
                    print("   âœ… Content authenticity VERIFIED!")
                    print("   ğŸ›¡ï¸  This content is confirmed AI-generated by MemeDici agent")
                else:
                    print("   âŒ Verification failed!")
                    print(f"   ğŸ“‹ Failed checks: {[k for k, v in verification_result.get('checks', {}).items() if not v]}")
                
                # Step 5: Display verification details
                print("\nğŸ“‹ STEP 5: Verification Details")
                print("-" * 40)
                
                checks = verification_result.get("checks", {})
