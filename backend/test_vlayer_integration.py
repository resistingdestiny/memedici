    if not is_configured:
        print("   âš ï¸  vlayer not fully configured (VLAYER_API_KEY or VLAYER_CONTRACT_ADDRESS missing)")
        print("   â„¹ï¸  Continuing with mock verification for testing...")
    
    # Step 2: Get available agents
    print("\nğŸ“‹ STEP 2: Available Agents")
    print("-" * 40)
    
    agents = agent_registry.list_agents()
